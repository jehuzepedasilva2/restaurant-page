function createDish(imagePath, svgHTML="", title, description, price, reverse=false) {
  const contentContainer = CachedDOM.getContentContainer();
  
  const dishContainer = document.createElement("div");
  dishContainer.classList.add("dish-container");

  const dishImage = document.createElement("img");
  dishImage.src = imagePath;

  const svgContainer = document.createElement('div');
  svgContainer.classList.add("icon")
  svgContainer.innerHTML = svgHTML;

  const descriptionContainer = document.createElement("div");
  descriptionContainer.classList.add("description-container");

  const dishTitle = document.createElement("h1");
  dishTitle.classList.add("dish-title");
  dishTitle.textContent = title;

  const dishDescription = document.createElement("p");
  dishDescription.classList.add("description");
  dishDescription.textContent = description;

  const dishPrice = document.createElement("p");
  dishPrice.classList.add("dish-price");
  dishPrice.textContent = price;

  descriptionContainer.appendChild(dishTitle);
  if (svgHTML !== "") {
    descriptionContainer.appendChild(svgContainer);
  }
  descriptionContainer.appendChild(dishDescription);
  descriptionContainer.appendChild(dishPrice);

  if (!reverse) {
    dishContainer.appendChild(dishImage);
  }
  dishContainer.appendChild(descriptionContainer);
  if (reverse) {
    dishContainer.appendChild(dishImage);
  }

  contentContainer.appendChild(dishContainer);
}

